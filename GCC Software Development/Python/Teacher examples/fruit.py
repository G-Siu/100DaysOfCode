# List containing 3 elements
fruits = ["apple", "banana", "kiwi", "cherry", "oranges"]

for each_item in fruits:
    if each_item == "banana" :
        continue
    if each_item == "cherry" :
        break
    print(f"I like {each_item}s")



# 1st iteration apple is passed to the variable fruit
# 2nd iteration banana is passed to the variable fruit
# 3rd iteration cherry is passed to the variable fruit
# 4th iteration orange is passed to the variable fruit