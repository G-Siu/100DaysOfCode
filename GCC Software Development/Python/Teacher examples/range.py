# Range using one parameter

# This starts at 0 and up to but excluding 10

for i in range(10) :
    print(i)

# Range using two parameters

# This start at 1 and up to but excluding 10

for j in range(1, 10) :
    print(j)

# Ranger using three parameters

# This start at 1 and up jumps every 2nd number
# to but excluding 10

for k in range(1, 10, 2) :
    print(k)